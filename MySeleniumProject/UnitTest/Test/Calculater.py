class Count():
    def __init__(self, x, y):
        self.x = x
        self.y = y

    def add(self):
        self.r1 = self.x + self.y
        return self.r1

    def sub(self):
        self.r2 = self.x - self.y
        return self.r2

    def mul(self):
        self.r3 = self.x * self.y
        return self.r3

    def div(self):
        self.r4 = self.x / self.y
        return self.r4




